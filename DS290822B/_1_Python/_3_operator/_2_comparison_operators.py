# Comparison Operator - returns bool value
# == - whether both the values are equal
# # !=
# >
# <
# >=
# <=

no1 = 10
no2 = 10

equal = no1 == no2
print("Equal : ",equal)

not_equal = no1 != no2
print("Not Equal : ",not_equal)

less_than = no1 < no2
print("Less Than : ",less_than)

greater_than = no1 > no2
print("Greater Than : ",greater_than)

less_than_equal_to = no1 <= no2 # here we checking if value is less/equal to another value
print("Less than equal to : ",less_than_equal_to)

greater_than_equal_to = no1 >= no2 # here we checking if value is greater/equal to another value
print("Greater than equal to : ",greater_than_equal_to)