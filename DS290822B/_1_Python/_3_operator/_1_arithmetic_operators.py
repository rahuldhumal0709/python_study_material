# Arithmetic Operators

# +
# -
# *
# /
# % - Modulus (Mod) - it returns remainder
# / / - Floor Divison - it returns quotient but as a int
# ** - Exponentia - power of, raise to

no1 = 20 # Hardcoded value
no2 = 7

add = no1 + no2
print("Addition : ",add)

sub = no1 - no2
print("Subtraction : ",sub)

mul = no1 * no2
print("Multiplication : ",mul)

# Division always returns float value
div = no1 / no2
print("Division : ",div)

mod = no1 % no2
print("Modulus : ",mod)

floor_div = no1 // no2
print("Floor Divison : ",floor_div)

expo = no1 ** 3
print("Exponentia : ",expo)