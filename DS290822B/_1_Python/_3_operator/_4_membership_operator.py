# Membership Operator - returns bool value
# in      - returns True if specified object is present in sequence of object
# not in  - returns True if specified object is not present in sequence of object 

no1 = 1
lst = [3,2,7,8,5,1]

in_demo = no1 in lst
print("In demo : ",in_demo)

not_in_demo = no1 not in lst
print("Not in demo : ",not_in_demo)