# File Handling
# - it is used to store the data permanently in the file

# Types of files
# 1. Text File (.txt,.doc)
# 2. Binary File (images,audio,video)

# Modes of text files
# r - read mode(allows to read the data from file), bydefault mode, it doesnot create the file if it doesn't exist.
# w - write mode(allows to write the data in file), it creates the file if doesn't exist. If it already exist, it will override it.
# a - append mode(allows to append the data in file), it creates the file if doesn't exist. If it already exist, it will not override it.
# r+ - same as r mode but also allows to write the data
# w+ - same as w mode but also allows to read the data
# a+ - same as a mode but also allows to read the data

# Modes for binary files
# rb
# wb
# ab
# rb+
# wb+
# ab+


